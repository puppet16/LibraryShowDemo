package com.luck.libraryshowdemo.bean;

public class ExcelPanelRowTitle {

    private String dateString;
    private String weekString;
    private int totalPersonalNum;

    public String getDateString() {
        return dateString;
    }

    public void setDateString(String dateString) {
        this.dateString = dateString;
    }

    public String getWeekString() {
        return weekString;
    }

    public void setWeekString(String weekString) {
        this.weekString = weekString;
    }

    public int getTotalPersonalNum() {
        return totalPersonalNum;
    }

    public void setTotalPersonalNum(int totalPersonalNum) {
        this.totalPersonalNum = totalPersonalNum;
    }
}
